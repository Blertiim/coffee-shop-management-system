const PDFDocument = require("pdfkit");
const prisma = require("../../config/prisma");
const { syncProductStockAlert } = require("../../services/alert.service");
const AppError = require("../../utils/app-error");
const {
  handleControllerError,
  sendError,
  sendSuccess,
} = require("../../utils/response");
const {
  validateCreateSupplierOrderPayload,
  validateSupplierOrderId,
  validateUpdateSupplierOrderPayload,
} = require("./supplier-order.validation");

const supplierOrderInclude = {
  supplier: true,
  employee: true,
  items: {
    include: {
      product: {
        include: {
          category: true,
        },
      },
    },
    orderBy: {
      id: "asc",
    },
  },
};

const INVOICE_DATE_FORMATTER = new Intl.DateTimeFormat("en-GB", {
  dateStyle: "short",
  timeStyle: "short",
});

const MONEY_FORMATTER = new Intl.NumberFormat("en-US", {
  minimumFractionDigits: 2,
  maximumFractionDigits: 2,
});

const formatMoney = (value) => MONEY_FORMATTER.format(Number(value || 0));

const buildSupplierInvoiceFileName = (supplierOrder) =>
  `incoming-invoice-${supplierOrder.invoiceNumber || supplierOrder.id}.pdf`
    .replace(/[^a-z0-9_.-]+/gi, "-")
    .toLowerCase();

const streamSupplierInvoicePdf = (res, supplierOrder) => {
  const doc = new PDFDocument({
    size: "A4",
    margin: 36,
  });

  doc.pipe(res);

  doc.fontSize(20).text("Incoming Supplier Invoice", { align: "center" });
  doc.moveDown(0.4);
  doc
    .fontSize(10)
    .fillColor("#555555")
    .text(`Invoice: ${supplierOrder.invoiceNumber || `#${supplierOrder.id}`}`, { align: "center" })
    .text(`Status: ${supplierOrder.status}`, { align: "center" });

  doc.moveDown(1);
  doc.fillColor("#111111").fontSize(11);
  doc.text(`Supplier: ${supplierOrder.supplier?.companyName || "-"}`);
  doc.text(`Contact: ${supplierOrder.supplier?.contactName || "-"}`);
  doc.text(`Phone: ${supplierOrder.supplier?.phone || "-"}`);
  doc.text(`Order date: ${INVOICE_DATE_FORMATTER.format(new Date(supplierOrder.orderDate))}`);
  doc.text(
    `Received: ${
      supplierOrder.receivedAt
        ? INVOICE_DATE_FORMATTER.format(new Date(supplierOrder.receivedAt))
        : "-"
    }`
  );

  if (supplierOrder.notes) {
    doc.text(`Notes: ${supplierOrder.notes}`);
  }

  doc.moveDown(1);
  doc.fontSize(10).text("Items", { underline: true });
  doc.moveDown(0.4);

  const tableTop = doc.y;
  const columns = {
    product: 36,
    quantity: 250,
    unitPrice: 335,
    total: 450,
  };

  doc.fontSize(9).fillColor("#333333");
  doc.text("Product", columns.product, tableTop);
  doc.text("Qty", columns.quantity, tableTop);
  doc.text("Unit Price", columns.unitPrice, tableTop);
  doc.text("Line Total", columns.total, tableTop);
  doc.moveTo(36, tableTop + 14).lineTo(558, tableTop + 14).strokeColor("#cccccc").stroke();

  let rowY = tableTop + 22;
  doc.fillColor("#111111");

  supplierOrder.items.forEach((item) => {
    const productName = item.product?.name || `Product #${item.productId}`;
    const unit = item.unit || item.product?.stockUnit || "cope";
    const lineTotal = Number(item.quantity || 0) * Number(item.unitPrice || 0);

    if (rowY > 745) {
      doc.addPage();
      rowY = 36;
    }

    doc.text(`${productName} #${item.productId}`, columns.product, rowY, {
      width: 200,
    });
    doc.text(`${item.quantity} ${unit}`, columns.quantity, rowY, { width: 70 });
    doc.text(`${formatMoney(item.unitPrice)} EUR`, columns.unitPrice, rowY, { width: 95 });
    doc.text(`${formatMoney(lineTotal)} EUR`, columns.total, rowY, { width: 100 });
    rowY += 24;
  });

  doc.moveTo(36, rowY + 4).lineTo(558, rowY + 4).strokeColor("#cccccc").stroke();
  doc.moveDown(1.5);
  doc.fontSize(13).text(`TOTAL: ${formatMoney(supplierOrder.total)} EUR`, {
    align: "right",
  });

  doc.moveDown(2);
  doc
    .fontSize(9)
    .fillColor("#555555")
    .text("Generated by Coffee Shop Management System.", { align: "center" });

  doc.end();
};

const ensureSupplierExists = async (supplierId) => {
  const supplier = await prisma.supplier.findUnique({
    where: { id: supplierId },
    select: { id: true },
  });

  return Boolean(supplier);
};

const ensureEmployeeExists = async (employeeId) => {
  if (employeeId === null || employeeId === undefined) {
    return true;
  }

  const employee = await prisma.employee.findUnique({
    where: { id: employeeId },
    select: { id: true },
  });

  return Boolean(employee);
};

const ensureProductsExist = async (tx, items) => {
  const products = await tx.product.findMany({
    where: {
      id: {
        in: items.map((item) => item.productId),
      },
      deletedAt: null,
    },
    select: {
      id: true,
      name: true,
      stockUnit: true,
    },
  });

  const productsById = new Map(products.map((product) => [product.id, product]));

  for (const item of items) {
    if (!productsById.has(item.productId)) {
      throw new AppError(`Product with id ${item.productId} was not found`, 404);
    }
  }

  return productsById;
};

const withItemUnits = (items, productsById) =>
  items.map((item) => ({
    ...item,
    unit: item.unit || productsById.get(item.productId)?.stockUnit || "cope",
  }));

const applySupplierOrderStock = async (tx, supplierOrder) => {
  if (supplierOrder.receivedAt || supplierOrder.status !== "delivered") {
    return null;
  }

  for (const item of supplierOrder.items) {
    const product = await tx.product.update({
      where: { id: item.productId },
      data: {
        stock: {
          increment: item.quantity,
        },
        stockUnit: item.unit || "cope",
      },
    });

    await syncProductStockAlert(product, undefined, tx);
  }

  return tx.supplierOrder.update({
    where: { id: supplierOrder.id },
    data: {
      receivedAt: new Date(),
    },
    include: supplierOrderInclude,
  });
};

exports.getAllSupplierOrders = async (req, res) => {
  try {
    const supplierOrders = await prisma.supplierOrder.findMany({
      include: supplierOrderInclude,
      orderBy: [{ orderDate: "desc" }, { createdAt: "desc" }],
    });

    return sendSuccess(
      res,
      200,
      "Supplier orders retrieved successfully",
      supplierOrders
    );
  } catch (error) {
    return handleControllerError(res, error, "Get all supplier orders error");
  }
};

exports.getSupplierOrderById = async (req, res) => {
  try {
    const id = validateSupplierOrderId(req.params.id);

    const supplierOrder = await prisma.supplierOrder.findUnique({
      where: { id },
      include: supplierOrderInclude,
    });

    if (!supplierOrder) {
      return sendError(res, 404, "Supplier order not found");
    }

    return sendSuccess(
      res,
      200,
      "Supplier order retrieved successfully",
      supplierOrder
    );
  } catch (error) {
    return handleControllerError(res, error, "Get supplier order by id error");
  }
};

exports.downloadSupplierInvoicePdf = async (req, res) => {
  try {
    const id = validateSupplierOrderId(req.params.id);

    const supplierOrder = await prisma.supplierOrder.findUnique({
      where: { id },
      include: supplierOrderInclude,
    });

    if (!supplierOrder) {
      return sendError(res, 404, "Supplier order not found");
    }

    res.setHeader("Content-Type", "application/pdf");
    res.setHeader(
      "Content-Disposition",
      `attachment; filename="${buildSupplierInvoiceFileName(supplierOrder)}"`
    );

    streamSupplierInvoicePdf(res, supplierOrder);
    return undefined;
  } catch (error) {
    return handleControllerError(res, error, "Download supplier invoice PDF error");
  }
};

exports.createSupplierOrder = async (req, res) => {
  try {
    const data = validateCreateSupplierOrderPayload(req.body);

    const supplierExists = await ensureSupplierExists(data.supplierId);

    if (!supplierExists) {
      return sendError(res, 404, "Supplier not found");
    }

    const employeeExists = await ensureEmployeeExists(data.employeeId);

    if (!employeeExists) {
      return sendError(res, 404, "Employee not found");
    }

    const supplierOrder = await prisma.$transaction(async (tx) => {
      const productsById = await ensureProductsExist(tx, data.items);
      const items = withItemUnits(data.items, productsById);

      const createdSupplierOrder = await tx.supplierOrder.create({
        data: {
          supplierId: data.supplierId,
          employeeId: data.employeeId,
          invoiceNumber: data.invoiceNumber,
          orderDate: data.orderDate,
          expectedDate: data.expectedDate,
          total: data.total,
          status: data.status,
          notes: data.notes,
          items: {
            create: items,
          },
        },
        include: supplierOrderInclude,
      });

      return (await applySupplierOrderStock(tx, createdSupplierOrder)) || createdSupplierOrder;
    });

    return sendSuccess(
      res,
      201,
      "Supplier order created successfully",
      supplierOrder
    );
  } catch (error) {
    return handleControllerError(res, error, "Create supplier order error");
  }
};

exports.updateSupplierOrder = async (req, res) => {
  try {
    const id = validateSupplierOrderId(req.params.id);
    const data = validateUpdateSupplierOrderPayload(req.body);

    const existingSupplierOrder = await prisma.supplierOrder.findUnique({
      where: { id },
      include: {
        items: true,
      },
    });

    if (!existingSupplierOrder) {
      return sendError(res, 404, "Supplier order not found");
    }

    if (
      existingSupplierOrder.receivedAt &&
      Object.keys(data).some((field) => field !== "notes")
    ) {
      return sendError(
        res,
        400,
        "Delivered supplier invoices cannot be changed after stock is applied"
      );
    }

    if (data.supplierId !== undefined) {
      const supplierExists = await ensureSupplierExists(data.supplierId);

      if (!supplierExists) {
        return sendError(res, 404, "Supplier not found");
      }
    }

    if (data.employeeId !== undefined) {
      const employeeExists = await ensureEmployeeExists(data.employeeId);

      if (!employeeExists) {
        return sendError(res, 404, "Employee not found");
      }
    }

    const supplierOrder = await prisma.$transaction(async (tx) => {
      if (data.items !== undefined) {
        const productsById = await ensureProductsExist(tx, data.items);
        data.items = withItemUnits(data.items, productsById);
      }

      const { items, ...supplierOrderData } = data;
      const updatedSupplierOrder = await tx.supplierOrder.update({
        where: { id },
        data: {
          ...supplierOrderData,
          ...(items !== undefined
            ? {
                items: {
                  deleteMany: {},
                  create: items,
                },
              }
            : {}),
        },
        include: supplierOrderInclude,
      });

      return (await applySupplierOrderStock(tx, updatedSupplierOrder)) || updatedSupplierOrder;
    });

    return sendSuccess(
      res,
      200,
      "Supplier order updated successfully",
      supplierOrder
    );
  } catch (error) {
    return handleControllerError(res, error, "Update supplier order error");
  }
};

exports.deleteSupplierOrder = async (req, res) => {
  try {
    const id = validateSupplierOrderId(req.params.id);

    const existingSupplierOrder = await prisma.supplierOrder.findUnique({
      where: { id },
    });

    if (!existingSupplierOrder) {
      return sendError(res, 404, "Supplier order not found");
    }

    if (existingSupplierOrder.receivedAt) {
      return sendError(
        res,
        400,
        "Delivered supplier invoices cannot be deleted after stock is applied"
      );
    }

    await prisma.supplierOrder.delete({
      where: { id },
    });

    return sendSuccess(res, 200, "Supplier order deleted successfully", null);
  } catch (error) {
    return handleControllerError(res, error, "Delete supplier order error");
  }
};
